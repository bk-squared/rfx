def _make_replay_fake_run(acc_data: dict, run_idx: int):
    """Return a sim.run replacement that replays captured accumulators.

    The real FDTD scanner is NEVER called.  dft_planes is populated from
    pre-captured complex64 arrays.  Under enable_x64(True) the assembly
    calls jnp.asarray(..., dtype=complex_dtype) where complex_dtype evaluates
    to complex128, so the c64 replayed arrays are upcast to c128 — exactly
    what the float64 equivalence gate tests.
    """
    def fake_run(self, *, n_steps=None, num_periods=1.0, compute_s_params=False):
        del n_steps, num_periods, compute_s_params
        planes = {}
        for key, arr in acc_data.items():
            if not key.startswith(f"run{run_idx}__"):
                continue
            plane_name = key[len(f"run{run_idx}__"):]
            planes[plane_name] = SimpleNamespace(accumulator=arr)
        return SimpleNamespace(dft_planes=planes)
    return fake_run


def _load_replay_accumulators():
    """Load captured accumulator .npz and return (data_dict, freqs)."""
    npz = np.load(REPLAY_ACCUMULATORS_PATH)
    freqs = npz["freqs"]
    data = {k: npz[k] for k in npz.files if k != "freqs"}
    return data, freqs


def _run_assembly_with_replay(acc_data: dict, freqs_replay: np.ndarray, *,
                               use_x64: bool) -> np.ndarray:
    """Run compute_msl_s_matrix assembly with replayed accumulators.

    The real FDTD is never called.  Only the post-run assembly (V/I
    integration and de-embedding) runs under the requested x64 setting.
    """
    from jax.experimental import enable_x64

    sim = _build_thru_line_sim()
    freqs_jnp = jnp.asarray(freqs_replay, dtype=jnp.float32)

    run_counter = [0]

    def counting_fake_run(self, *, n_steps=None, num_periods=1.0,
                           compute_s_params=False):
        r = run_counter[0]
        run_counter[0] += 1
        fake = _make_replay_fake_run(acc_data, r)
        return fake(self, n_steps=n_steps, num_periods=num_periods,
                    compute_s_params=compute_s_params)

    sim.run = MethodType(counting_fake_run, sim)

    with enable_x64(use_x64):
        result = sim.compute_msl_s_matrix(
            n_steps=1,
            freqs=freqs_jnp,
            num_periods=1.0,
        )

    return np.asarray(result.S, dtype=np.complex128)


# ---------------------------------------------------------------------------
# Test A: replay float64 equivalence (FAST — runs in default gate)
# ---------------------------------------------------------------------------

def test_replay_float64_equivalence():
    """Assembly under x64 on replayed c64 accumulators must match numpy-f64 golden.

    Gate: atol=1e-5.  Both jnp-x64 and numpy assemble from the SAME captured
    complex64 FDTD accumulators upcast to complex128 — structural equivalence
    must hold to near-machine-epsilon (observed ~1e-13).

    This test NEVER invokes the real FDTD scanner under x64.  sim.run is
    monkeypatched to return replayed plane data directly.  Only the post-run
    V/I integration and de-embedding assembly runs under x64.

    x64 is scoped via jax.experimental.enable_x64 context manager — does NOT
    leak to other test modules in the same pytest session.
    """
    acc_data, freqs_replay = _load_replay_accumulators()
    golden_f64 = np.load(REPLAY_GOLDEN_F64_PATH)  # (2, 2, n_freqs), complex128

    s_jnp_f64 = _run_assembly_with_replay(acc_data, freqs_replay, use_x64=True)

    print("\n[test_replay_float64_equivalence] per-frequency S overlay")
    print(f"{'freq_idx':>8}  {'max|S_jnp-S_numpy|':>20}")
    for fi in range(s_jnp_f64.shape[-1]):
        dev_fi = float(np.max(np.abs(s_jnp_f64[:, :, fi] - golden_f64[:, :, fi])))
        jv = s_jnp_f64[:, :, fi]
        nv = golden_f64[:, :, fi]
        print(f"  f[{fi:02d}]  dev={dev_fi:.3e}")
        print(f"         jnp  S00={jv[0,0].real:+.10e}{jv[0,0].imag:+.10e}j  "
              f"S10={jv[1,0].real:+.10e}{jv[1,0].imag:+.10e}j")
        print(f"         npy  S00={nv[0,0].real:+.10e}{nv[0,0].imag:+.10e}j  "
              f"S10={nv[1,0].real:+.10e}{nv[1,0].imag:+.10e}j")

    max_abs_dev = float(np.max(np.abs(s_jnp_f64 - golden_f64)))
    print(f"\n[test_replay_float64_equivalence] max_abs_dev = {max_abs_dev:.3e}")
    assert max_abs_dev < 1e-5, (
        f"jnp-x64 assembly differs from numpy-f64 golden by {max_abs_dev:.3e} "
        f"(threshold 1e-5). Structural divergence beyond float-point associativity."
    )
    np.testing.assert_allclose(s_jnp_f64, golden_f64, rtol=1e-5, atol=1e-5)
    print("[test_replay_float64_equivalence] PASS")


# ---------------------------------------------------------------------------
# Test B: float32 deployment delta documented and bounded (FAST)
# ---------------------------------------------------------------------------

def test_float32_deployment_delta_bounded():
    """Document and gate the production float32 vs float64 assembly delta.

    The production jnp assembly runs in float32 (no global x64).  The
    independent numpy golden runs in float64 (complex128).  The delta is
    physically negligible — MSL Z0 staircase error at DX=80µm is >5%.

    x64 is scoped via context manager; does not leak to other modules.
    """
    acc_data, freqs_replay = _load_replay_accumulators()

    s_f64 = _run_assembly_with_replay(acc_data, freqs_replay, use_x64=True)
    s_f32 = _run_assembly_with_replay(acc_data, freqs_replay, use_x64=False)

    delta = float(np.max(np.abs(s_f64 - s_f32)))
    print(f"\n[test_float32_deployment_delta_bounded] "
          f"f32-vs-f64 max_abs_dev = {delta:.3e}")
    print("  (Production float32 delta vs float64 structural result.)")
    print("  MSL Z0 staircase physics error at DX=80µm is >5% — delta is negligible.")

    assert delta < 1e-3, (
        f"f32-vs-f64 deployment delta {delta:.3e} exceeds 1e-3. "
        f"Unexpected precision regression."
    )
    print("[test_float32_deployment_delta_bounded] PASS")


